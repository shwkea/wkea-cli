function block(title: string, items: [string, string][]): string {
  const lines: string[] = [`  ◆ ${title}`];
  for (const [k, v] of items) {
    lines.push(`    ${k}`);
    const pairs = v.match(/\d+=[^\s]+/g) || [];
    for (const p of pairs) {
      lines.push(`      ${p}`);
    }
  }
  return lines.join('\n');
}

